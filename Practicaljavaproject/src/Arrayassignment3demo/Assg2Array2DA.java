package Arrayassignment3demo;

public class Assg2Array2DA {

	public static void main(String[] args) {
		char ch[][]= {{'a','b','c'},{'d','e','f'},{'g','h','i'}};

		for(int i=0;i<ch.length/2;i++)

		{

		 for(int j=0;j<ch[i].length;j++)

		 {

			 System.out.print(ch[i][j]+" ");

		 }

		 

		}

	}

}
