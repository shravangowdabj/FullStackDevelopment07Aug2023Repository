package Arrayassignment3demo;

public class Assg4D2ArrayRow {

	public static void main(String[] args) {
		int a[][]= {{10,20,30},{40,50,60},{70,80,90}};

		for(int i=1;i<2;i++)

		{

		 for(int j=0;j<a[i].length;j++)

		 {

			 System.out.print(a[i][j]+" ");

		 }

		 System.out.println();

		}

	}

}
