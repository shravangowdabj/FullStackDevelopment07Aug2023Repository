package Arrayassignment3demo;

public class Assg42DArrayA {

	public static void main(String[] args) {
		char ch[][]= {{'a','b','c'},{'d','e','f'},{'g','h','i'}};

		for(int i=0;i<ch.length;i++)

		{

		 for(int j=0;j<ch[i].length/2;j++)

		 {

			 System.out.print(ch[i][j]+" ");

		 }

		 System.out.println();



	}

	}

}
